CREATE TABLE SPASAVATELJ (
  ID            INT PRIMARY KEY AUTO_INCREMENT,
  IME           VARCHAR(50),
  BROJ_TELEFONA VARCHAR(15),
  SPECIJALNOST  INT,
  ISKUSTVO      INT,
  LOKACIJA_LAT  VARCHAR(20),
  LOKACIJA_LNG  VARCHAR(20),
  AKTIVAN       BOOL,
  USERNAME      VARCHAR(25),
  PWORD         VARCHAR(25),
  FB_TOKEN      VARCHAR(100),
  DEV_TOKEN     VARCHAR(500),
  IS_RESCUER    BOOL
);

CREATE TABLE AKCIJA (
  ID_A          INT PRIMARY KEY AUTO_INCREMENT,
  VODITELJ_BROJ VARCHAR(15),
  LOK_LAT       VARCHAR(20),
  LOK_LNG       VARCHAR(20),
  RADIUS        DOUBLE,
  OPIS          VARCHAR(512),
  AKTIVNA       BOOL,
  LOC_MEETING   VARCHAR(100),
  TIME_MEETING  VARCHAR(10),
  PRIO          INT
);

CREATE TABLE SPASAVATELJ_AKCIJA (
  ID_SPASAVATELJ INT,
  ID_AKCIJA      INT,
  PRIHVATIO      BOOL,
  FOREIGN KEY (ID_SPASAVATELJ) REFERENCES PUBLIC.SPASAVATELJ (ID),
  FOREIGN KEY (ID_AKCIJA) REFERENCES PUBLIC.AKCIJA (ID_A)
);

CREATE TABLE USER_LOCATION (
  ID_USER   INT,
  TIMESTAMP BIGINT,
  LOC_LAT   VARCHAR(20),
  LOC_LNG   VARCHAR(20),
  FOREIGN KEY (ID_USER) REFERENCES PUBLIC.SPASAVATELJ (ID),
);

CREATE TABLE TITLE (
  ID_TITLE INT PRIMARY KEY AUTO_INCREMENT,
  VALUE    VARCHAR(50)
);

